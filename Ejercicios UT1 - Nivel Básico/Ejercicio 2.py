##Escribe un programa en Python que pida al usuario dos números y
##luego imprima la suma, resta, multiplicación y división de ambos números.

num1 = float(input("Introduce el primer número: "))
num2 = float(input("Introduce el segundo número: "))

suma = num1 + num2
print(f"Suma: {suma}")
resta = num1 - num2
print(f"Resta: {resta}")
multiplicacion = num1 * num2
print(f"Multiplicación: {multiplicacion}")
division = num1 / num2
print(f"División: {division}")